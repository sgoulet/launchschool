#  What is exception handling and what problem does it solve?

#  Exception handling handles some kind of unpredictability in a predictable and manageable way.  Ruby uses the Exception class
#  to handle the unpredictable behavior in a manageable fashion.  The begin-rescue-end reserved words comprise ruby's syntactic
#  structure through exception handling.  The flow of control is handled through this syntactic structure so that the program
#  does not come to an abrupt stop.